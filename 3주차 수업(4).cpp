#include <iostream>
using namespace std;
int main()
{
	int i;
	double f;
	f = 5 / 4;
	cout << f << endl;
	f = (double)5 / 4;
	cout << f << endl;
	f = 5 / (double)4;
	cout << f << endl;
	f = (double)5 / (double)4;
	cout << f << endl;
	i = 1.3 + 1.8;
	cout << i << endl;
	i = (int)1.3 + (int)1.8;
	cout << i << endl;

}